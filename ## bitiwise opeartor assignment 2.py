#program to express the various type of bitwise opeartor
#1. & bitwise opeartor
a=7
b=4
print(a&b)
#2. | bitwise opeartor
e=45
g=67
print(e|g)
#3. ^ bitwise opeartor
w=23
d=44
print(w^d)
#4. ~ bitwise opeartor
h=67
k=78
print(~h)
print(~k)
#5. << left shift bitwise opeartor
A=12
B=23
print(A<<B)
print(B<<A)
print(A<<12)
print(B<<23)
#6. >> right shift bitwise opeartor
Q=12
S=21
print(Q>>S)
print(S>>Q)
print(S>>21)
print(Q>>12)
print(Q>>24)

